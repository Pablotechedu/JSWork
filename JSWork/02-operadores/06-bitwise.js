let resultadoAnd = 5 & 3; // 0101 & 0011 = 0001
console.log(resultadoAnd); // Resultado: 1


let resultadoOr = 5 | 3; // 0101 | 0011 = 0111
console.log(resultadoOr); // Resultado: 7

let resultadoXor = 5 ^ 3; // 0101 ^ 0011 = 0110
console.log(resultadoXor); // Resultado: 6

let resultadoShiftinfL = 5 << 1; // 0101 << 1 = 1010
console.log(resultadoShiftinfL); // Resultado: 10

let resultadoShiftingR = -5 >> 1; // 11111111111111111111111111111011 >> 1 = 11111111111111111111111111111101
console.log(resultadoShiftingR); // Resultado: -3
